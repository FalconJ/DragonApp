package com.example.falconp.dndapp.Entidades;

public class Spell {

    private String name;
    private String dice;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDice() {
        return dice;
    }

    public void setDice(String dice) {
        this.dice = dice;
    }
}
